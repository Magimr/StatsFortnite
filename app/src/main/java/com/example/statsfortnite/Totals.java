package com.example.statsfortnite;

public class Totals {

    private long kills;
    private long wins;
    private long matchesplayed;
    private long minutesplayed;
    private long hoursplayed;
    private long score;
    private float winrate;
    private float kd;
    private long lastupdate;

    public long getKills() {
        return kills;
    }

    public void setKills(long kills) {
        this.kills = kills;
    }

    public long getWins() {
        return wins;
    }

    public void setWins(long wins) {
        this.wins = wins;
    }

    public long getMatchesplayed() {
        return matchesplayed;
    }

    public void setMatchesplayed(long matchesplayed) {
        this.matchesplayed = matchesplayed;
    }

    public long getMinutesplayed() {
        return minutesplayed;
    }

    public void setMinutesplayed(long minutesplayed) {
        this.minutesplayed = minutesplayed;
    }

    public long getHoursplayed() {
        return hoursplayed;
    }

    public void setHoursplayed(long hoursplayed) {
        this.hoursplayed = hoursplayed;
    }

    public long getScore() {
        return score;
    }

    public void setScore(long score) {
        this.score = score;
    }

    public float getWinrate() {
        return winrate;
    }

    public void setWinrate(float winrate) {
        this.winrate = winrate;
    }

    public float getKd() {
        return kd;
    }

    public void setKd(float kd) {
        this.kd = kd;
    }

    public long getLastupdate() {
        return lastupdate;
    }

    public void setLastupdate(long lastupdate) {
        this.lastupdate = lastupdate;
    }
}
